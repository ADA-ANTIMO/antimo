import Foundation

extension SignedInteger {
  var toInt8: Int8 {
    Int8(self)
  }

  var toInt16: Int16 {
    Int16(self)
  }

  var toInt32: Int32 {
    Int32(self)
  }

  var toInt64: Int64 {
    Int64(self)
  }

  var toInt: Int {
    Int(self)
  }
}
